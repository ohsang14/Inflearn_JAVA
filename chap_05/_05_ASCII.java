package chap_05;

public class _05_ASCII {
    public static void main(String[] args) {
        //아스키 코드( ANSI): 미국 표준 코드
        char c = 'A';
        System.out.println(c);
        System.out.println((int)c);

        c = 'B';

        System.out.println(c);
        System.out.println((int)c);

        c++;
        System.out.println(c);
        System.out.println((int)c);
    }
}
